// Define the numbers and the operation here
let number1 = 20;
let number2 = 10;
let operation = "+";

// Write if-statements here
if (operation === "+") 
{
    console.log(` ${number1} ${operation} ${number2} = ${number1 +  number2 }`);

} else if(operation === "-")
{
    console.log(` ${number1} ${operation} ${number2} = ${number1 -  number2 }`);

} else if(operation === "*")
{
    console.log(` ${number1} ${operation} ${number2} = ${number1 *  number2 }`);

} else if (operation === "/")
{
    console.log(` ${number1} ${operation} ${number2} = ${number1 /  number2 }`);

} else {
    console.log("Undefine operation");
    
}


